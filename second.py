# Integers and floats
a = 5
b = 2.5
print(a, type(a))      # 5 <class 'int'>
print(b, type(b))      # 2.5 <class 'float'>

# Strings
greeting = "Hello, Python!"
print(greeting, type(greeting))  # Hello, Python! <class 'str'>

# Booleans
flag = True
print(flag, type(flag))          # True <class 'bool'>
